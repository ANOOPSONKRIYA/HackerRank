#include <iostream>   // Required for cin and cout

using namespace std;

int main() {
    // Declare three integer variables
    int n1, n2, n3;

    // Read three integers from standard input
    cin >> n1 >> n2 >> n3;

    // Output the sum of the three integers
    cout << n1 + n2 + n3;

    return 0;  // Indicates successful program execution
}
